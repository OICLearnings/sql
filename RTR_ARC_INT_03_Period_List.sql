CREATE TABLE RTR_ARC_INT_03_Period_List (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY MINVALUE 1 
	MAXVALUE 999999999 INCREMENT BY 1 START WITH 100 CACHE 20 NOORDER NOCYCLE NOKEEP NOSCALE,
    Period_Name VARCHAR(255) NOT NULL,
	Prior_Period VARCHAR(255) NOT NULL,
    Start_Date DATE NOT NULL,
    End_Date DATE NOT NULL,
    Close_Date DATE,
	FREQUENCY VARCHAR(255) NOT NULL,
    Created_by VARCHAR(255) DEFAULT 1420556 ,
    Creation_Date DATE DEFAULT SYSDATE,
    Last_Update_Date DATE DEFAULT SYSDATE,
    Last_updated_by VARCHAR(255)
);
